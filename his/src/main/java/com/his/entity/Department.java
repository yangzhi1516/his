package com.his.entity;

import java.io.Serializable;
/**
 * 	科室表
 * @author 杨培源
 *
 */
public class Department implements Serializable{
    /**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	private Integer departmentId;	//科室编号

    private String departmentName;	//科室名称

    private Integer departmentNumber;	//科室人数

    private Integer departmentStaffId;	//科室主任id

    private Double departmentCost;	//挂号费用
    
    public Integer getDepartmentId() {
        return departmentId;
    }

    public void setDepartmentId(Integer departmentId) {
        this.departmentId = departmentId;
    }

    public String getDepartmentName() {
        return departmentName;
    }

    public void setDepartmentName(String departmentName) {
        this.departmentName = departmentName == null ? null : departmentName.trim();
    }

    public Integer getDepartmentNumber() {
        return departmentNumber;
    }

    public void setDepartmentNumber(Integer departmentNumber) {
        this.departmentNumber = departmentNumber;
    }

    public Integer getDepartmentStaffId() {
        return departmentStaffId;
    }

    public void setDepartmentStaffId(Integer departmentStaffId) {
        this.departmentStaffId = departmentStaffId;
    }

	public Double getDepartmentCost() {
		return departmentCost;
	}

	public void setDepartmentCost(Double departmentCost) {
		this.departmentCost = departmentCost;
	}
    
    
}